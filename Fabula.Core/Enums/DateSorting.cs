namespace Fabula.Core.Enums;

/// <summary>
/// Enum containing possible sorting strategies for entities based on the date they were posted.
/// </summary>

public enum DateSorting
{
    /// <summary>
    /// Oldest entities first
    /// </summary>
    
    Oldest = 0,

    /// <summary>
    /// Newest entities first
    /// </summary>

    Newest = 1
}
